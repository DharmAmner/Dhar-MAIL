generate_username() {
    prefixes=("jose" "maria" "juan" "laura" "carlos" "ana" "david" "marta")
    suffixes=("1980" "1990" "2000" "2020" "latino" "chico" "chica" "top")
    
    prefix=${prefixes[$RANDOM % ${#prefixes[@]}]}
    suffix=${suffixes[$RANDOM % ${#suffixes[@]}]}
    
    echo "${prefix}.${suffix}"
}

generate_emails() {
    local domain=$1
    local count=$2
    
    for ((i = 0; i < count; i++)); do
        username=$(generate_username)
        echo "${username}@${domain}" >> "/storage/emulated/0/emails.txt"
    done
}

